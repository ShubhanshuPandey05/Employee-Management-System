import mongoose from "mongoose";

const managerSchema = new mongoose.Schema({
    name:{
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true,
        unique:true
    },
    password:{
        type:String,
        required:true,
        minLength:6
    },
})

const Manager = new mongoose.model("manager",managerSchema);
export default Manager;